# Faça um algoritmo que receba três números e 
# exiba a média aritmética

termo1 = float(input('Informe o primeiro termo: '))
termo2 = float(input('Informe o segundo termo: '))
termo3 = float(input('Informe o terceiro termo: '))

media = (termo1 + termo2 + termo3) / 3 
print(f"A média dos termos é: {media:.2f}")