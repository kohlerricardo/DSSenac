# Faça um algoritmo que receba dois números e exiba o resultado da divisão do
# primeiro pelo segundo.
termo1 = int(input("Informe o primeiro termo: "))
termo2 = int(input("Informe o segundo termo: "))
div = termo1 /  termo2 
print(f"o resultado é {div:.0f}")
print(f"o resultado é {div:.2f}")
print(f"o resultado é {div:.5f}")
